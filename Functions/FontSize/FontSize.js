module.exports = font = (fontSelected) => {
    let fontSize;
    switch (fontSelected) {
        case "Noteworthy":
            return fontSize = "28";
            break;
        case "Baskerville":
            return fontSize = "40";
            break;
        case "Funnier":
            return fontSize = "42";
            break;
        case "Futura Bold":
            return fontSize = "40";
            break;
        case "Myriad Pro Bold":
            return fontSize = "35";
            break;
        case "EmbossedTapeThree":
            return fontSize = "35pt ";
            break;
        case "Noteworhty Bold":
            return fontSize = "28";
            break;
    }
};

