module.exports = font = (fontSelected) => {
    let fontSize;
    switch (fontSelected) {
        case "Noteworthy":
            return fontSize = "32pt";
            break;
        case "Baskerville":
            return fontSize = "40pt";
            break;
        case "Funnier":
            return fontSize = "38pt";
            break;
        case "Futura Bold":
            return fontSize = "40pt";
            break;
        case "Myriad Pro Bold":
            return fontSize = "39pt";
            break;
        case "EmbossedTapeThree":
            return fontSize = "35pt";
            break;
        case "Noteworhty Bold":
            return fontSize = "40pt";
            break;
        case "MyriadPro-Bold":
            return fontSize = "39pt";
            break;
        case "Embossing":
            return fontSize = "36pt";
            break;
    }
};

