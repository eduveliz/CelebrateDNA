module.exports = fontStyle = (fontSelected) => {
    let fontStyle;
    switch (fontSelected) {
        case "Noteworthy":
            return fontStyle = "Noteworthy";
            break;
        case "Baskerville":
            return fontStyle = "Baskerville";
            break;
        case "Funnier":
            return fontStyle = 'Funnier';
            break;
        case "Futura Bold":
            return fontStyle = "Futura";
            break;
        case "Myriad Pro Bold":
            return fontStyle = "MyriadPro-Bold";
            break;
        case "MyriadProBold":
            return fontStyle = "MyriadPro-Bold";
            break;
        case "EmbossedTapeThree":
            return fontStyle = "Embossing";
            break;
        case "Noteworthy Bold":
            return fontStyle = "Noteworthy";
            break;
        case "NoteworthyBold":
            return fontStyle = "Noteworthy";
            break;
    }
};
