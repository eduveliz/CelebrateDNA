module.exports = colorProduct = (colorSelected) => {
    let color;
    switch (colorSelected) {
        case "Heather Prism Mint":
            return color = "#9BCFAC";
            break;
        case "Ash":
            return color = "#ECEDE5";
            break;
        case "White":
            return color = 'white';
            break;
        case "Heather Blue":
            return color = "#C0CEEB";
            break;
        case "Steel Blue":
            return color = "#335E7F";
            break;
        case "Kelly":
            return color = "#166E47";
            break;
        case "Navy":
            return color = "#2A2738";
            break;
        case "Asphalt":
            return color = "#595957";
            break;
        case "Forest":
            return color = "#36503A";
            break;
        case "Army":
            return color = "#5C5047";
            break;
        case "Silver":
            return color = "#C2C4C8";
            break;
        case "Indigo Blue":
            return color = "#3F6075";
            break;
        case "Black":
            return color = "Black";
            break;
    }
};
