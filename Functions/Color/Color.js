module.exports = colorProduct = (colorSelected) => {
    let color;
    switch (colorSelected) {
        case "Heather Prism Mint":
            return color = "#9BCFAC";
            break;
        case "Ash":
            return color = "#ECEDE5";
            break;
        case "White":
            return color = 'white';
            break;
        case "Heather Blue":
            return color = "#C0CEEB";
            break;
        case "Steel Blue":
            return color = "#335E7F";
            break;
        case "Kelly":
            return color = "#166E47";
            break;
        case "Navy":
            return color = "#2A2738";
            break;
    }
};
