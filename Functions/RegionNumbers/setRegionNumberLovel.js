module.exports = regionNumber = (cantidad) => {
    if (cantidad === 7) {
        return 3
    }
    if (cantidad === 9) {
        return 4;
    }
    if (cantidad === 11) {
        return 5;
    }
    if (cantidad === 13) {
        return 6;
    }
    if (cantidad === 15) {
        return 7;
    }
    if (cantidad === 17) {
        return 8;
    }
    if (cantidad === 19) {
        return 9;
    }
    if (cantidad === 21) {
        return 10;
    }
};
