module.exports = regionNumber = (cantidad) => {
    if (cantidad === 10) {
        return 3
    }
    if (cantidad === 12) {
        return 4;
    }
    if (cantidad === 14) {
        return 5;
    }
    if (cantidad === 16) {
        return 6;
    }
    if (cantidad === 18) {
        return 7;
    }
    if (cantidad === 20) {
        return 8;
    }
    if (cantidad === 22) {
        return 9;
    }
    if (cantidad === 24) {
        return 10;
    }
};
